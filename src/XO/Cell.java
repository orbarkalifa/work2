package XO;

// this class will represent a cell using 2 integer coordinates
public class Cell {

    private final int row, col;

    public Cell(int x, int y){
        row=x;
        col=y;
    }

    public int getRow() {
        return row;
    }
    public int getCol() {
        return col;
    }



}
